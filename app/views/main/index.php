<script>
    $(document).ready(function () {
        var securityMail = 0;

        $('[name=email]').tooltip('disable');

        $('[name=email]').on('keyup', function () {
            var $this = $(this);

            var email = $this.val();
            var regMail = /^[0-9a-z-\.]+\@[0-9a-z-]{1,}\.[a-z]{2,}$/i;
            var checkMail = regMail.exec(email);

            if (checkMail == null) {
                $this.css({
                    'border': '1px solid #fbc2c4',
                    'color': '#8a1f11'
                });
                securityMail = 0;
            } else {
                $this.css({
                    'border': '1px solid #ccc',
                    'color': '#495057'
                });
                securityMail = 1;
            }
        });

        $('#button-addon1').on('click', function (e) {
            e.preventDefault();
            if (securityMail == 1) {
                $('#spinnerRegModal').modal('show');
                $('#regForm1').submit();
            } else {
                e.preventDefault();
                $(this).parents('form').find('[name=email]').tooltip('enable').tooltip('show');
                $(this).parents('form').find('[name=email]').css({
                    'border': '1px solid #fbc2c4',
                    'color': '#8a1f11'
                });
                setTimeout(function () {
                    $('[name=email]').tooltip('disable').tooltip('hide');
                }, 2000);
            }
        });

        $('#button-addon2').on('click', function (e) {
            e.preventDefault();
            if (securityMail == 1) {
                $('#spinnerRegModal').modal('show');
                $('#regForm2').submit();
            } else {
                e.preventDefault();
                $(this).parents('form').find('[name=email]').tooltip('enable').tooltip('show');
                $(this).parents('form').find('[name=email]').css({
                    'border': '1px solid #fbc2c4',
                    'color': '#8a1f11'
                });
                setTimeout(function () {
                    $('[name=email]').tooltip('disable').tooltip('hide');
                }, 2000);
            }
        });
    });

</script>